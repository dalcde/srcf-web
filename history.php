<h1> Past Schedules </h1>
<p>
When I'm stuck on a past paper question, I'm frequently frustrated by the fact that it might not be because I'm stupid, but because it's no longer taught. So I searched through the <a href="http://archive.org">Internet Archive</a> and dug out the past course specifications, and made a changelog. The following list shows how the exam paper of that year differs from the previous year since 2007. This is obtained purely from diff-ing the schedules provided, and may not accurately portrait what was actually taught.</p>
<h2>Part IB</h2>
Haven't found the time to do any detailed analysis, but here are some random notes I've noted. These are not guaranteed to be correct and are very disorganized.
<ul>
  <li>Before 2010, Variational Principles did not exist. Instead, the parts on Euler-Lagrange equations and Lagrange multipliers were put in Methods (the physicsy things and second variation are not found anywhere afaik). As a result, before 2010, Methods did not include the PDEs on unbounded domains part. Before 2007, Methods did not include Fourier transforms, and instead contained the Cartesian tensor things from IA Vector Calculus.</li>
  <li>Before 2010, Numerical Analysis was a 12 lecture course and did not include the part on ODE's. Relevant past paper questions can be found in the Part II Numerical Analysis course.</li>
  <li>Before 2015, Electromagnetism did not have the part on relativity and had something on Drude's model of conductivity.</li>
</ul>

<h2>Part IA</h2>
I am not updating any changes from 2016 onwards, since I'm no longer doing IA, unless some kind soul emails me about the changes they have found.
<h3>Vectors and matrices</h3>
<ul>
  <li>2008:&thinsp;<s>Born out of thin air.</s> Split from <i>Algebra and Geometry</i> and contains most non-groups contents from Algebra and geometry.</li>
</ul>
<h3>Analysis I</h3>
<ul>
  <li>2014:&thinsp;Removed statement of Taylor's theorem from C to C.</li>
  <li>2007:&thinsp;Removed least upper bounds and least upper bound axiom; sequences and series; convergence of bounded monotonic sequence (moved to Numbers and Sets).</li>
</ul>
<h3>Differential Equations</h3>
<ul>
  <li>2011:&thinsp;Added phase plane analysis of equilibrium points and stabilities, directional derivatives and gradient vector.</li>
  <li>2008:&thinsp;Added informal treatment of big-O and small-o notation and l'Hopital's rule. Added Abel's theorem</li>
  <li>2007:&thinsp;Added exact differentials.</li>
</ul>
<h3>Probability</h3>
<ul>
  <li>2013:&thinsp;Removed addition theorem, Boole and Bonferroni inequalities. Added Jacobian in transformation of random variables and statement (without proof) of continuity theorem.</li>
</ul>
<h3>Groups</h3>
<ul>
  <li>2010:&thinsp;Added relation between general and special linear groups with the Mobius group, simplicity of A<sub>5</sub>. Removed symmetries of the dodecahedron.</li>
  <li>2008:&thinsp;Split from <i>Algebra and Geometry</i>. Added Cayley's theorem (every group is isomorphic to a subgroup of a permutation group), conjugacy classes and Cauchy's theorem; proof in R<sup>3</sup> that every element of the orthogonal group is a product of reflections and every rotation in R<sup>3</sup> has an axis, basis change as an example of conjugation; conjugacy in S<sub>n</sub> and in A<sub>n</sub>, simple groups, A<sub>5</sub> and the symmetries of the dodecahedron.</li>
</ul>
<h3>Vector Calculus</h3>
<ul>
  <li>2008:&thinsp;Added statement of Maxwell's equations.</li>
  <li>2007:&thinsp;Added scalar potential</li>
</ul>
<h3>Numbers and Sets</h3>
<ul>
  <li>2007:&thinsp;Added least upper bounds and least upper bound axiom; sequences and series; convergence of bounded monotonic sequence.</li>
</ul>
<h3>Dynamics and Relativity</h3>
<ul>
  <li>2011:&thinsp;Removed inertia tensor; Foucault pendulum not examinable.</li>
  <li>2009:&thinsp;Summoned by the faculty to replace <i>Newtonian Dynamics</i>. Removed phase plane dynamics and replaced with an introduction to Special Relativity.
</ul>
